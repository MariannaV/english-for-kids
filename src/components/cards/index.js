export const wordSetCards = {
  createCard: (data) => {
    const card = document.createElement('a');
    card.classList.add('category-card');
    card.dataset.setId = data.setId;
    card.insertAdjacentHTML(
      'beforeend',
      `
          <img src=${data.src}>
          <p>${data.text}</p>`
    );
    return card;
  },
};

